# Operadores de comparação são utilizados para comparar dois valores e retorna um valor booleano ou seja false ou true.

x = 200
y = 400
i = 200

print(x == y)

print(x > y) # x maior que y

print(x < y) # x menor que y 

print(x != y) # x diferente de y

print(i >= x) # i maior ou igual a x

print(i <= x) # i menor ou igual a x
